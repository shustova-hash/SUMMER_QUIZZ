function calculateProfile(answers) {
  // answers is an array of selected codes ('A', 'B', 'V', 'G')
  let countA = 0;
  let countB = 0;
  let countV = 0;
  let countG = 0;

  answers.forEach(code => {
    if (code === 'A') countA++;
    else if (code === 'B') countB++;
    else if (code === 'V') countV++;
    else if (code === 'G') countG++;
  });

  const countDev = countA + countV; // A and V map to Developer/GameDev
  const countArtist = countB;      // B maps to Digital Artist/Creator

  if (countDev >= 5) {
    return PROFILES.DEV;
  } else if (countArtist >= 4) {
    return PROFILES.ARTIST;
  } else {
    return PROFILES.EXPLORER;
  }
}
